public class ForLoop {
    public static void main(String[] args) {
        // for loop
        for (int i = 1; i <= 10; i++) { // increment by 1
            System.out.println(i);
        }

            System.out.println("");

        for (int i = 10; i >0; i--) { // decrement by 1
            System.out.println(i);
        }
            System.out.println("");


        for (int i = 1; i <= 10; i+=2) { // increment by 2
            System.out.println(i);
        }
    }
}
